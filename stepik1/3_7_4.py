cor = {'север': 0, 'восток': 0, 'юг': 0, 'запад': 0}

for i in range(int(input())):
    s = [j for j in input().split()]
    cor[s[0]] += int(s[1])

print(cor['восток']-cor['запад'], cor['север']-cor['юг'])
